        <div class="row" style="padding-right: 5em; padding-left: 5em; padding-top: 2em">
            <p>“The XquarOne DH delivers an incredible amount of traction, especially while cornering. Typically, it’s pretty easy to let the back end get drifty when summer is in full swing and the trails are dry and dusty – all it takes is a subtle weight shift in the right scenario and the rear wheel of almost any bike out there will break free and slide around like a gymkhana car. With the XquarOne DH it’s a different story – that rear tire feels absolutely glued to the ground, no matter how hard you push into it. It’s a wild feeling, one that quickly leads to increased confidence when heading into a sharp turn with a good head of steam.</p><br>
            <p>That massive amount of traction had me expecting to need to fight to get airborne, but the XquarOne responded relatively well. There’s not as much pop as you’d find with something like a YT Tues, but it didn’t take a massive amount of effort to take flight either. My abbreviated stint on this bike left me with plenty of unanswered questions – I’m curious about how the bike will feel in really rough, choppy terrain, and about how the swingarm elements will fare when it comes to durability, but I’ll need more time on the bike before I can figure out the answers.”</p>
